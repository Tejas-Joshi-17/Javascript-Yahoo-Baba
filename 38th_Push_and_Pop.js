// 4) pop( )         :-  Same like pop_back( )

let Arr_2 = [1, 2, 3, 4]

console.log(`Array Before Pop :- ${Arr_2}`)

// Poping
Arr_2.pop()

console.log(`Array After Pop :- ${Arr_2}`)

// Array Before Pop :- 1, 2, 3, 4
// Array After Pop :- 1, 2, 3


