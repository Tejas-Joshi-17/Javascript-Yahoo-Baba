// 3) push( )        :-  Same like push_back( )

let Arr_1 = [1, 2, 3, 4]

console.log(`Array Before Push :- ${Arr_1}`)

// Pushing
Arr_1.push(100)
Arr_1.push(200)

console.log(`Array After Push :- ${Arr_1}`)

// Array Before Push :- 1, 2, 3, 4
// Array After Push :- 1, 2, 3, 4, 100, 200


