var a = 1;
var b = "1";

console.log(a==b)        // true        <----- Only Check Value not Datatype
console.log(a===b)       // false       <----- Check Value & Datatype both

console.log(a!=b)        // false       <----- Only Check Value not Datatype
console.log(a!==b)       // true        <----- Check Value & Datatype both