// 6) shift( )         :-  Same like pop_front( )

let Arr_2 = [1, 2, 3, 4]

console.log(`Array Before Shift :- ${Arr_2}`)

// Shifting
Arr_2.shift()

console.log(`Array After Shift :- ${Arr_2}`)

// Array Before Pop :- 1, 2, 3, 4
// Array After Pop :- 2, 3, 4


