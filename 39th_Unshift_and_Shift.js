// 5) unshift( )     :-  Same like push_front( )

let Arr_1 = [1, 2, 3, 4]

console.log(`Array Before Unshift :- ${Arr_1}`)

// Unshifting
Arr_1.unshift(100)

console.log(`Array After Unshift :- ${Arr_1}`)

// Array Before Push :- 1, 2, 3, 4
// Array After Push :- 100, 1, 2, 3, 4


