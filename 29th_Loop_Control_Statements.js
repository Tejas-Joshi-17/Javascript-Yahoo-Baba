
// Loop Control Statements :- 
// ----- 1) Break
// ----- 2) Continue




// 1) Break
for(var a = 1; a<=5; a++){
    if(a==4)
        break
    console.log(a + ") Tejas Joshi")
}
// 1) Tejas Joshi
// 2) Tejas Joshi
// 3) Tejas Joshi




// 2) Continue
for(var a = 1; a<=5; a++){
    if(a==4)
        continue
    console.log(a + ") Tejas Joshi")
}
// 1) Tejas Joshi
// 2) Tejas Joshi
// 3) Tejas Joshi
// 5) Tejas Joshi