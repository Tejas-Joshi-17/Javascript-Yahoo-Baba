
// Logical Operators

var a = 10
var b = 20

if(a<b && a > 10)
    console.log(a + " is less than " + b + " & " + a + " is greater than 10")

if(a<b && a >= 10)
    console.log(a + " is less than " + b + " & " + a + " is greater than or equal to 10")
// 10 is less than 20 & 10 is greater than or equal to 10